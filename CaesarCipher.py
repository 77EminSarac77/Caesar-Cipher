alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q',
            'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z', 'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h',
            'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z'
            ]


def caesar(message, num_shift, encrypt_decrypt):
    copy_alphabet = alphabet.copy()
    change_text = ""
    keep = False
    if encrypt_decrypt == "decode":
        num_shift *= -1
    for index in range(0, len(message)):
        letter = message[index]
        if letter not in alphabet:
            keep = True
        if not keep:
            shifted_letter = copy_alphabet.pop(num_shift + alphabet.index(letter))
            change_text += shifted_letter
            copy_alphabet = alphabet.copy()
        if keep:
            change_text += letter
        keep = False
    print(f"The {encrypt_decrypt}d text is", change_text)


def main():
    direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
    text = input("Type your message:\n").lower()
    shift = int(input("Type the shift number:\n"))
    shift = shift % 26
    caesar(message=text, num_shift=shift, encrypt_decrypt=direction)
    rerun_program = input("Type \'yes\' if you want to go again. Otherwise type \'no\'.\n")
    while rerun_program == 'yes':
        main()
    print("Goodbye")
    exit()


main()


